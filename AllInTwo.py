import math

# Задача 1
x = int(input("Введите пятизначное число: "))
print(x // 10000)
print((x // 1000) % 10)
print((x // 100) % 10)
print((x // 10) % 10)
print(x % 10)

# Задача 2
side_a = 5
side_b = 6
side_c = 7
p = (side_a + side_b + side_c) / 2
S = str(math.sqrt(p * (p - side_a) * (p - side_b) * (p - side_c)))
print('Площадь треугольника равна ' + S)

# Задача 3
r = float(input("Введите радиус окружности: "))
L = 2 * math.pi * r
print('Длина окружности равна ' + str(L))
